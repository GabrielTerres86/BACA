BEGIN
  UPDATE CRAPTAB
     SET DSTEXTAB = 0
   WHERE CDCOOPER = 3
     AND UPPER(NMSISTEM) = 'CRED'
     AND UPPER(TPTABELA) = 'GENERI'
     AND CDEMPRES = 0
     AND UPPER(CDACESSO) = 'DIF_FUSO_CABINEPIX'
     AND TPREGIST = 0;
  COMMIT;
EXCEPTION
  WHEN OTHERS THEN
    Sistema.excecaoInterna(pr_compleme => 'PIX_CONTABIL');
END;
