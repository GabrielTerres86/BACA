
-- Create sequence
CREATE SEQUENCE SEQ_PREAPROV_IDLIMITE
MINVALUE 1
MAXVALUE 99999999999999
START WITH 1
INCREMENT BY 1
NOCACHE
ORDER;


CREATE OR REPLACE TRIGGER CECRED.trg_preaprov_idlimite
  BEFORE INSERT ON TBCRD_PREAPROV_LIMITE
  FOR EACH ROW
  WHEN (NEW.idlimite IS NULL)
BEGIN
  :NEW.idlimite := seq_preaprov_idlimite.NEXTVAL;
END;