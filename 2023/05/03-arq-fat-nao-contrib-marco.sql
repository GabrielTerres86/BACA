                                           ,pr_interacao IN OUT NUMBER) RETURN BOOLEAN IS
    BEGIN
      pr_interacao := pr_interacao + 1;
      
      IF pr_interacao != 1 THEN
        DBMS_LOCK.SLEEP(10); 
      END IF;
      
      IF pr_interacao < 7 THEN
        IF cecred.GENE0001.fn_exis_arquivo(pr_caminho => pr_caminho) THEN 
          RETURN TRUE;
        ELSE
