      vr_utlfileh := vr_arquivo_log;
      vr_dsmensagen := pr_dsmensagen;
    END IF;    
    sistema.escrevelinhaarquivo(pr_utlfileh => vr_utlfileh, 
                                pr_des_text => vr_dsmensagen);
  
  END; 

  
