DECLARE
BEGIN
  UPDATE TBGEN_ANALISE_FRAUDE A SET A.CDPARECER_ANALISE = 2, A.FLGANALISE_MANUAL = 1 
   WHERE A.DTMVTOLT = to_date('08/12/2023', 'dd/mm/yyyy')
