BEGIN
	UPDATE CECRED.TBCADAST_PESSOA a
	SET a.NRDCONTA = b.NRDCONTA
	FROM CECRED.crapass b
	INNER JOIN CECRED.TBSITE_COOPERADO_CDC cdc ON cdc.NRDCONTA = b.NRDCONTA AND cdc.CDCOOPER = b.CDCOOPER 
	WHERE a.NRDCONTA <> b.NRDCONTA and a.NRCPFCGC = b.NRCPFCGC and a.CDCOOPER = b.CDCOOPER;

	COMMIT;
EXCEPTION WHEN OTHERS THEN
	ROLLBACK;
END;