UPDATE CRAPFDC FDC
SET FDC.INCHEQUE = 5,
    FDC.DTLIQCHQ = TO_DATE('21/01/2019', 'DD/MM/RRRR')  
WHERE FDC.CDCOOPER = 12
  AND FDC.NRDCONTA = 3700
  AND FDC.NRCHEQUE = 519
  AND FDC.PROGRESS_RECID = 52928694;
  
  
UPDATE CRAPFDC FDC
SET FDC.INCHEQUE = 5,
    FDC.DTLIQCHQ = TO_DATE('21/01/2019', 'DD/MM/RRRR')  
WHERE FDC.CDCOOPER = 12
  AND FDC.NRDCONTA = 62120
  AND FDC.NRCHEQUE = 56
  AND FDC.PROGRESS_RECID = 52104253;
  
COMMIT;  
