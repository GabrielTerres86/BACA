BEGIN
  UPDATE TBDOMIC_LIQTRANS_ARQUIVO
     SET DHRECEBIMENTO = SYSDATE
   WHERE TRUNC(DHRECEBIMENTO) = TRUNC(SYSDATE)
     AND LENGTH(NMARQUIVO_ORIGEM) = 31;

  UPDATE TBDOMIC_LIQTRANS_LANCTO
     SET INSITUACAO = 1
        ,DHINCLUSAO = SYSDATE
   WHERE IDARQUIVO IN (SELECT IDARQUIVO
                         FROM TBDOMIC_LIQTRANS_ARQUIVO
                        WHERE TRUNC(DHRECEBIMENTO) = TRUNC(SYSDATE));
  COMMIT;
END;
