BEGIN
	DELETE FROM TBCC_LANCAMENTOS_PENDENTES
	 WHERE IDSEQ_LANCAMENTO IN
		   (SELECT
			 L.IDSEQ_LANCAMENTO
			  FROM TBCC_LANCAMENTOS_PENDENTES L
			 WHERE L.IDSEQ_LANCAMENTO > 71617807
			   AND L.IDSITUACAO IN ('I', 'M', 'E', 'A')
			   AND EXISTS (SELECT 1
					  FROM TBCC_LANCAMENTOS_PENDENTES L2
					 WHERE L2.IDSEQ_LANCAMENTO <> L.IDSEQ_LANCAMENTO
					   AND L2.IDTRANSACAO = L.IDTRANSACAO
					   AND L2.CDPRODUTO = L.CDPRODUTO
					   AND L2.CDHISTOR = L.CDHISTOR
					   AND L2.CDCOOPER = L.CDCOOPER
					   AND L2.NRDCONTA = L.NRDCONTA
					   AND L2.IDSITUACAO = 'P'));


	DELETE FROM TBCC_LANCAMENTOS_PENDENTES
	 WHERE IDSEQ_LANCAMENTO IN
		   (SELECT
			 L.IDSEQ_LANCAMENTO
			  FROM TBCC_LANCAMENTOS_PENDENTES L
			 WHERE L.IDSEQ_LANCAMENTO > 71617807
			   AND EXISTS (SELECT 1
					  FROM TBCC_LANCAMENTOS_PENDENTES L2
					 WHERE L2.IDSEQ_LANCAMENTO <> L.IDSEQ_LANCAMENTO
					   AND L2.IDTRANSACAO = L.IDTRANSACAO
					   AND L2.CDPRODUTO = L.CDPRODUTO
					   AND L2.CDHISTOR = L.CDHISTOR
					   AND L2.CDCOOPER = L.CDCOOPER
					   AND L2.NRDCONTA = L.NRDCONTA
					   AND L2.DHPROCESSO <= L.DHPROCESSO
					   AND L2.IDSITUACAO = L.IDSITUACAO));

	
	DELETE FROM TBCC_LANCAMENTOS_PENDENTES
	 WHERE IDSEQ_LANCAMENTO IN
		   (SELECT
			 L.IDSEQ_LANCAMENTO
			  FROM TBCC_LANCAMENTOS_PENDENTES L
			 WHERE L.IDSEQ_LANCAMENTO > 71617807
			   AND L.DTMVTOLT < TRUNC(SYSDATE)
			   AND L.IDSITUACAO IN ('I')
			   AND EXISTS (SELECT 1
						FROM TBCC_LANCAMENTOS_PENDENTES L2
					   WHERE L2.IDSEQ_LANCAMENTO <> L.IDSEQ_LANCAMENTO
						 AND L2.IDTRANSACAO = L.IDTRANSACAO
						 AND L2.CDPRODUTO = L.CDPRODUTO
						 AND L2.CDHISTOR = L.CDHISTOR
						 AND L2.CDCOOPER = L.CDCOOPER
						 AND L2.NRDCONTA = L.NRDCONTA
						 AND L2.IDSITUACAO <> 'P'));


	COMMIT;
END;