DECLARE
  VN_EXISTE NUMBER := 0;
BEGIN
  BEGIN
    SELECT COUNT(1)
      INTO VN_EXISTE
      FROM ALL_TABLES T
     WHERE T.OWNER = 'TARIFAS'
       AND T.TABLE_NAME = 'TB_DOMINIO';
  EXCEPTION
    WHEN OTHERS THEN
      VN_EXISTE := 0;
  END;
  --
  IF VN_EXISTE > 0 THEN
    EXECUTE IMMEDIATE 'DROP TABLE TARIFAS.TB_DOMINIO';
  END IF;
  --
  COMMIT;
  --
EXCEPTION
  WHEN OTHERS THEN
    ROLLBACK;
END;
/