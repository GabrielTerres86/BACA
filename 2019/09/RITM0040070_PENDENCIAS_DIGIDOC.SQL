BEGIN
	--Retirar a pendencia do digidoc do tipo de documento 8
	UPDATE CRAPDOC DOC
	SET DOC.TPBXAPEN = 1, DOC.DTBXAPEN = SYSDATE
	WHERE DOC.TPDOCMTO IN (8) AND DOC.DTBXAPEN IS NULL AND DOC.TPBXAPEN = 0 
	AND DOC.DTMVTOLT > TO_DATE('02/04/2019','DD/MM/YYYY')
	AND DOC.DTMVTOLT < TO_DATE('04/07/2019','DD/MM/YYYY')
	AND NOT EXISTS (SELECT ASS.NRDCONTA FROM CRAPASS ASS 
				   WHERE ASS.CDCOOPER = DOC.CDCOOPER AND ASS.NRDCONTA = DOC.NRDCONTA
				   AND ASS.INPESSOA <> 1);               
  COMMIT;
END;			   
