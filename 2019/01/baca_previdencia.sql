  CURSOR cr_ass (pr_cdcooper IN crapass.cdcooper%TYPE
                ,pr_nrcpfcgc IN crapass.nrcpfcgc%TYPE) IS
    SELECT nrdconta FROM crapass
     WHERE cdcooper = pr_cdcooper
       AND nrcpfcgc = pr_nrcpfcgc
       AND dtdemiss IS NULL
       AND inpessoa = 1;
  rw_ass cr_ass%ROWTYPE;    
  
  PROCEDURE add_reg (pr_nrdconta IN crapass.nrdconta%TYPE
                    ,pr_cdcooper IN crapcop.cdcooper%TYPE
                    ,pr_nrcpfcgc IN VARCHAR2
                    ,pr_dtadesao IN VARCHAR2) IS
    idx PLS_INTEGER;
                        
  BEGIN
    idx := reg.count() + 1;
    reg(idx).nrdconta := pr_nrdconta;
    reg(idx).cdcooper := pr_cdcooper;
    reg(idx).nrcpfcgc := to_number(REPLACE(pr_nrcpfcgc,'-',''));
    reg(idx).dtadesao := to_date(pr_dtadesao,'DD/MM/RRRR');
  END;
  
  PROCEDURE del_reg (pr_cdcooper IN crapcop.cdcooper%TYPE
                    ,pr_nrdconta IN crapass.nrdconta%TYPE) IS
    idx PLS_INTEGER;                        
  BEGIN
    dbms_output.put (pr_cdcooper || ' - ' || pr_nrdconta || ' => DEL ');
        
    BEGIN
      DELETE FROM tbprevidencia_conta 
      WHERE cdcooper = pr_cdcooper
        AND nrdconta = pr_nrdconta;
        
      IF SQL%ROWCOUNT > 0 THEN
        dbms_output.put_line('OK');        
      ELSE
        dbms_output.put_line('NOK');        
      END IF;
      
    EXCEPTION
      WHEN OTHERS THEN
        dbms_output.put_line('NOK - ' || SQLERRM);        
    END;
  END;  
  
